/** Generate auth API routes for the Vite plugin (JWT-based) */
export function generateAuthApiRoutes(): string {
  return [
    `/**`,
    ` * Auth API routes — JWT-based authentication.`,
    ` * POST /api/auth/register — Create account`,
    ` * POST /api/auth/login — Sign in, returns JWT`,
    ` * GET /api/auth/me — Get current user from token`,
    ` */`,
    `import type { Connect } from 'vite';`,
    ``,
    `interface StoredUser { id: number; email: string; name: string | null; password: string; createdAt: string; }`,
    `let nextUserId = 2;`,
    `const users: StoredUser[] = [`,
    `  { id: 1, email: 'demo@test.com', name: 'Demo User', password: 'password123', createdAt: new Date().toISOString() },`,
    `];`,
    ``,
    `const tokens = new Map<string, number>(); // token → userId`,
    ``,
    `function generateToken(): string {`,
    `  return Array.from(crypto.getRandomValues(new Uint8Array(32)), b => b.toString(16).padStart(2, '0')).join('');`,
    `}`,
    ``,
    `function safeUser(u: StoredUser) { return { id: u.id, email: u.email, name: u.name }; }`,
    ``,
    `function json(res: Connect.ServerResponse, data: unknown, status = 200) {`,
    `  res.statusCode = status;`,
    `  res.setHeader('Content-Type', 'application/json');`,
    `  res.end(JSON.stringify(data));`,
    `}`,
    ``,
    `function parseBody(req: Connect.IncomingMessage): Promise<Record<string, unknown>> {`,
    `  return new Promise((resolve) => {`,
    `    let body = '';`,
    `    req.on('data', (c: Buffer) => (body += c.toString()));`,
    `    req.on('end', () => resolve(body ? JSON.parse(body) : {}));`,
    `  });`,
    `}`,
    ``,
    `export function getUserFromToken(authHeader?: string): StoredUser | null {`,
    `  if (!authHeader?.startsWith('Bearer ')) return null;`,
    `  const token = authHeader.slice(7);`,
    `  const userId = tokens.get(token);`,
    `  if (!userId) return null;`,
    `  return users.find(u => u.id === userId) || null;`,
    `}`,
    ``,
    `export function handleAuthRoute(req: Connect.IncomingMessage, res: Connect.ServerResponse, url: string): boolean {`,
    `  if (url === '/api/auth/register' && req.method === 'POST') {`,
    `    parseBody(req).then(body => {`,
    `      const email = (body.email as string || '').trim().toLowerCase();`,
    `      const password = body.password as string || '';`,
    `      const name = (body.name as string) || null;`,
    `      if (!email || !email.includes('@')) return json(res, { error: 'Valid email required' }, 400);`,
    `      if (password.length < 8) return json(res, { error: 'Password must be at least 8 characters' }, 400);`,
    `      if (users.find(u => u.email === email)) return json(res, { error: 'Email already registered' }, 409);`,
    `      const user: StoredUser = { id: nextUserId++, email, name, password, createdAt: new Date().toISOString() };`,
    `      users.push(user);`,
    `      const token = generateToken();`,
    `      tokens.set(token, user.id);`,
    `      json(res, { token, user: safeUser(user) }, 201);`,
    `    });`,
    `    return true;`,
    `  }`,
    `  if (url === '/api/auth/login' && req.method === 'POST') {`,
    `    parseBody(req).then(body => {`,
    `      const email = (body.email as string || '').trim().toLowerCase();`,
    `      const password = body.password as string || '';`,
    `      const user = users.find(u => u.email === email && u.password === password);`,
    `      if (!user) return json(res, { error: 'Invalid email or password' }, 401);`,
    `      const token = generateToken();`,
    `      tokens.set(token, user.id);`,
    `      json(res, { token, user: safeUser(user) });`,
    `    });`,
    `    return true;`,
    `  }`,
    `  if (url === '/api/auth/me' && req.method === 'GET') {`,
    `    const user = getUserFromToken(req.headers.authorization as string);`,
    `    if (!user) return json(res, { error: 'Unauthorized' }, 401), true;`,
    `    json(res, safeUser(user));`,
    `    return true;`,
    `  }`,
    `  return false;`,
    `}`,
    ``,
  ].join('\n');
}

