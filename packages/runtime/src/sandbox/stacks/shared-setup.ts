/* ── CLI Setup Script Generator ──────────────────────────────────────── */

/**
 * Generates a `scripts/setup.mjs` file that walks the user through env setup.
 * Auto-generates secrets, validates formats, creates .env file.
 */
export function generateSetupScript(stack: string, tier: string): string {
  return [
    `#!/usr/bin/env node`,
    `/**`,
    ` * ${stack.toUpperCase()} ${tier} — Interactive .env Setup`,
    ` * Run: node scripts/setup.mjs`,
    ` */`,
    `import { createInterface } from 'node:readline';`,
    `import { randomBytes } from 'node:crypto';`,
    `import { existsSync, readFileSync, writeFileSync } from 'node:fs';`,
    ``,
    `const rl = createInterface({ input: process.stdin, output: process.stdout });`,
    `const ask = (q) => new Promise(r => rl.question(q, r));`,
    ``,
    `const COLORS = {`,
    `  reset: '\\x1b[0m', bold: '\\x1b[1m', dim: '\\x1b[2m',`,
    `  green: '\\x1b[32m', yellow: '\\x1b[33m', blue: '\\x1b[34m',`,
    `  cyan: '\\x1b[36m', red: '\\x1b[31m', magenta: '\\x1b[35m',`,
    `};`,
    `const c = (color, text) => \`\${COLORS[color]}\${text}\${COLORS.reset}\`;`,
    ``,
    `function genSecret(len = 64) { return randomBytes(len).toString('hex').slice(0, len); }`,
    `function genHex(len = 64) { return randomBytes(len / 2).toString('hex'); }`,
    ``,
    `console.log('');`,
    `console.log(c('bold', c('cyan', '  ╔═══════════════════════════════════════════╗')));`,
    `console.log(c('bold', c('cyan', '  ║') + '  🔧 ' + c('bold', '${stack.toUpperCase()} ${tier}') + ' — Environment Setup  ' + c('cyan', '║')));`,
    `console.log(c('bold', c('cyan', '  ╚═══════════════════════════════════════════╝')));`,
    `console.log('');`,
    ``,
    `// Load existing .env if present`,
    `let existing = {};`,
    `if (existsSync('.env')) {`,
    `  const lines = readFileSync('.env', 'utf-8').split('\\n');`,
    `  for (const line of lines) {`,
    `    const m = line.match(/^([A-Z_]+)=(.*)$/);`,
    `    if (m) existing[m[1]] = m[2].replace(/^["']|["']$/g, '');`,
    `  }`,
    `  console.log(c('dim', '  Found existing .env — will preserve values you skip.'));`,
    `  console.log('');`,
    `}`,
    ``,
    `const env = { ...existing };`,
    ``,
    `async function setupVar(key, label, description, opts = {}) {`,
    `  const current = existing[key];`,
    `  const { autoGen, defaultVal, required, providerUrl, howToGet, formatHint } = opts;`,
    `  `,
    `  console.log(c('bold', \`  \${label}\`) + c('dim', \` (\${key})\`));`,
    `  console.log(c('dim', \`  \${description}\`));`,
    `  if (formatHint) console.log(c('dim', \`  Format: \${formatHint}\`));`,
    `  if (providerUrl) console.log(c('blue', \`  Get it → \${providerUrl}\`));`,
    `  if (howToGet?.length) {`,
    `    for (const step of howToGet) console.log(c('dim', \`    \${step}\`));`,
    `  }`,
    `  `,
    `  if (autoGen) {`,
    `    if (current) {`,
    `      console.log(c('green', \`  ✓ Using existing value\`));`,
    `      env[key] = current;`,
    `    } else {`,
    `      const generated = autoGen === 'secret' ? genSecret() : autoGen === 'uuid' ? crypto.randomUUID() : '3000';`,
    `      console.log(c('green', \`  ✓ Auto-generated\`));`,
    `      env[key] = generated;`,
    `    }`,
    `    console.log('');`,
    `    return;`,
    `  }`,
    `  `,
    `  const prompt = current`,
    `    ? \`  Value [\${c('dim', current.slice(0, 20) + (current.length > 20 ? '...' : ''))}]: \``,
    `    : defaultVal`,
    `      ? \`  Value [\${c('dim', defaultVal)}]: \``,
    `      : required`,
    `        ? \`  Value \${c('red', '(required)')}: \``,
    `        : \`  Value \${c('dim', '(press Enter to skip)')}: \`;`,
    `  `,
    `  const answer = await ask(prompt);`,
    `  env[key] = answer.trim() || current || defaultVal || '';`,
    `  `,
    `  if (env[key]) {`,
    `    console.log(c('green', '  ✓ Set'));`,
    `  } else if (required) {`,
    `    console.log(c('yellow', '  ⚠ Skipped (required — app may not work without this)'));`,
    `  } else {`,
    `    console.log(c('dim', '  ○ Skipped'));`,
    `  }`,
    `  console.log('');`,
    `}`,
    ``,
    `async function main() {`,
    `  // Variables will be populated per-tier by the template generator`,
    `  // This is the template — actual vars injected at scaffold time`,
    `  %%SETUP_CALLS%%`,
    `  `,
    `  // Write .env`,
    `  const lines = Object.entries(env)`,
    `    .filter(([, v]) => v !== '')`,
    `    .map(([k, v]) => \`\${k}="\${v}"\`);`,
    `  writeFileSync('.env', lines.join('\\n') + '\\n');`,
    `  `,
    `  console.log(c('bold', c('green', '  ✅ .env file written!')));`,
    `  const setCount = Object.values(env).filter(Boolean).length;`,
    `  const totalCount = lines.length;`,
    `  console.log(c('dim', \`  \${setCount} variables configured.\`));`,
    `  console.log('');`,
    `  console.log(c('cyan', '  Next: npm run dev'));`,
    `  console.log('');`,
    `  rl.close();`,
    `}`,
    ``,
    `main().catch(err => { console.error(err); rl.close(); process.exit(1); });`,
    ``,
  ].join('\n');
}

/**
 * Generates the setup call lines for the CLI script based on tier.
 */
export function generateSetupCalls(envVars: EnvVar[], tier: string): string {
  const vars = envVars.filter((v) => v.tiers.includes(tier as any));
  const grouped = new Map<string, EnvVar[]>();
  for (const v of vars) {
    if (!grouped.has(v.category)) grouped.set(v.category, []);
    grouped.get(v.category)!.push(v);
  }

  const lines: string[] = [];
  for (const [category, vars] of grouped) {
    lines.push(`  console.log(c('bold', c('magenta', '  ── ${category.toUpperCase()} ──')));`);
    lines.push(`  console.log('');`);
    for (const v of vars) {
      const opts: Record<string, unknown> = {};
      if (v.autoGenerate) opts.autoGen = `'${v.autoGenerate}'`;
      if (v.defaultValue) opts.defaultVal = `'${v.defaultValue}'`;
      if (v.required) opts.required = true;
      if (v.providerUrl) opts.providerUrl = `'${v.providerUrl}'`;
      if (v.howToGet) opts.howToGet = JSON.stringify(v.howToGet);
      if (v.formatHint) opts.formatHint = `'${v.formatHint}'`;

      const optsStr = Object.entries(opts)
        .map(([k, v]) => `${k}: ${v}`)
        .join(', ');

      lines.push(
        `  await setupVar('${v.key}', '${v.label}', '${v.description.replace(/'/g, "\\'")}', { ${optsStr} });`,
      );
    }
    lines.push('');
  }
  return lines.join('\n');
}

/**
 * Generates the complete setup.mjs script for a given stack + tier.
 */
export function buildSetupScript(stack: string, tier: string, envVars: EnvVar[]): string {
  const template = generateSetupScript(stack, tier);
  const calls = generateSetupCalls(envVars, tier);
  return template.replace('%%SETUP_CALLS%%', calls);
}

