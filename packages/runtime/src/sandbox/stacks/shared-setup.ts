/**
 * Generates the server-side setup API routes for the web wizard.
 */
export function generateSetupApiRoutes(): string {
  return [
    `import type { Plugin, Connect } from 'vite';`,
    `import { existsSync, readFileSync, writeFileSync } from 'node:fs';`,
    `import { join } from 'node:path';`,
    ``,
    `function json(res: Connect.ServerResponse, data: unknown, status = 200) {`,
    `  res.statusCode = status;`,
    `  res.setHeader('Content-Type', 'application/json');`,
    `  res.end(JSON.stringify(data));`,
    `}`,
    `function parseBody(req: Connect.IncomingMessage): Promise<Record<string, unknown>> {`,
    `  return new Promise((resolve) => {`,
    `    let body = '';`,
    `    req.on('data', (c: Buffer) => (body += c.toString()));`,
    `    req.on('end', () => resolve(body ? JSON.parse(body) : {}));`,
    `  });`,
    `}`,
    ``,
    `function parseEnvFile(path: string): Record<string, string> {`,
    `  if (!existsSync(path)) return {};`,
    `  const result: Record<string, string> = {};`,
    `  for (const line of readFileSync(path, 'utf-8').split('\\n')) {`,
    `    const m = line.match(/^([A-Z_][A-Z0-9_]*)=["']?(.*)["']?$/);`,
    `    if (m) result[m[1]] = m[2].replace(/["']$/g, '');`,
    `  }`,
    `  return result;`,
    `}`,
    ``,
    `export function setupPlugin(): Plugin {`,
    `  return {`,
    `    name: 'setup-api',`,
    `    apply: 'serve',`,
    `    configureServer(server) {`,
    `      const envPath = join(process.cwd(), '.env');`,
    ``,
    `      server.middlewares.use(async (req, res, next) => {`,
    `        if (req.url === '/api/setup/status' && req.method === 'GET') {`,
    `          const exists = existsSync(envPath);`,
    `          const values = exists ? parseEnvFile(envPath) : {};`,
    `          // Mask sensitive values for display`,
    `          const masked = Object.fromEntries(`,
    `            Object.entries(values).map(([k, v]) => {`,
    `              if (k.includes('SECRET') || k.includes('KEY') || k.includes('PASSWORD')) {`,
    `                return [k, v.length > 8 ? v.slice(0, 4) + '••••' + v.slice(-4) : '••••••••'];`,
    `              }`,
    `              return [k, v];`,
    `            }),`,
    `          );`,
    `          return json(res, { exists, values: masked });`,
    `        }`,
    ``,
    `        if (req.url === '/api/setup/save' && req.method === 'POST') {`,
    `          const body = await parseBody(req);`,
    `          const envData = body.env as Record<string, string>;`,
    `          if (!envData || typeof envData !== 'object') {`,
    `            return json(res, { error: 'Invalid payload' }, 400);`,
    `          }`,
    `          const lines = Object.entries(envData)`,
    `            .filter(([, v]) => v && String(v).trim())`,
    `            .map(([k, v]) => \`\${k}="\${String(v).trim()}"\`);`,
    `          writeFileSync(envPath, lines.join('\\n') + '\\n');`,
    `          return json(res, { ok: true, count: lines.length });`,
    `        }`,
    ``,
    `        next();`,
    `      });`,
    `    },`,
    `  };`,
    `}`,
    ``,
  ].join('\n');
}

