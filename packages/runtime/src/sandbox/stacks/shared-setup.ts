/** Generate a JWT-based auth context provider for React apps */
export function generateAuthContext(): string {
  return [
    `import { createContext, useContext, useState, useCallback, useEffect, type ReactNode } from 'react';`,
    ``,
    `interface User { id: number; email: string; name: string | null; }`,
    `interface AuthContextType {`,
    `  user: User | null;`,
    `  loading: boolean;`,
    `  login: (email: string, password: string) => Promise<void>;`,
    `  register: (email: string, password: string, name?: string) => Promise<void>;`,
    `  logout: () => void;`,
    `  error: string | null;`,
    `}`,
    ``,
    `const AuthContext = createContext<AuthContextType>({`,
    `  user: null, loading: true, login: async () => {}, register: async () => {}, logout: () => {}, error: null,`,
    `});`,
    `export const useAuth = () => useContext(AuthContext);`,
    ``,
    `export function AuthProvider({ children }: { children: ReactNode }) {`,
    `  const [user, setUser] = useState<User | null>(null);`,
    `  const [loading, setLoading] = useState(true);`,
    `  const [error, setError] = useState<string | null>(null);`,
    ``,
    `  useEffect(() => {`,
    `    const token = localStorage.getItem('token');`,
    `    if (token) {`,
    `      fetch('/api/auth/me', { headers: { Authorization: \`Bearer \${token}\` } })`,
    `        .then(r => r.ok ? r.json() : Promise.reject())`,
    `        .then(setUser)`,
    `        .catch(() => localStorage.removeItem('token'))`,
    `        .finally(() => setLoading(false));`,
    `    } else { setLoading(false); }`,
    `  }, []);`,
    ``,
    `  const login = useCallback(async (email: string, password: string) => {`,
    `    setError(null);`,
    `    const res = await fetch('/api/auth/login', {`,
    `      method: 'POST', headers: { 'Content-Type': 'application/json' },`,
    `      body: JSON.stringify({ email, password }),`,
    `    });`,
    `    if (!res.ok) { const e = await res.json(); setError(e.error || 'Login failed'); return; }`,
    `    const { token, user: u } = await res.json();`,
    `    localStorage.setItem('token', token);`,
    `    setUser(u);`,
    `  }, []);`,
    ``,
    `  const register = useCallback(async (email: string, password: string, name?: string) => {`,
    `    setError(null);`,
    `    const res = await fetch('/api/auth/register', {`,
    `      method: 'POST', headers: { 'Content-Type': 'application/json' },`,
    `      body: JSON.stringify({ email, password, name }),`,
    `    });`,
    `    if (!res.ok) { const e = await res.json(); setError(e.error || 'Registration failed'); return; }`,
    `    const { token, user: u } = await res.json();`,
    `    localStorage.setItem('token', token);`,
    `    setUser(u);`,
    `  }, []);`,
    ``,
    `  const logout = useCallback(() => {`,
    `    localStorage.removeItem('token');`,
    `    setUser(null);`,
    `  }, []);`,
    ``,
    `  return (`,
    `    <AuthContext.Provider value={{ user, loading, login, register, logout, error }}>`,
    `      {children}`,
    `    </AuthContext.Provider>`,
    `  );`,
    `}`,
    ``,
  ].join('\n');
}

