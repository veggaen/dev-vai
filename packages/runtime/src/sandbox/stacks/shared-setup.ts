/**
 * Shared Setup Wizard — reusable .env setup + onboarding system for all stacks.
 *
 * Two modes:
 * 1. CLI mode: `npx vai-setup` — asks questions, generates .env
 * 2. Web mode: `/setup` route — guided wizard UI with deep links to provider consoles
 *
 * Scales by tier:
 * - Basic: just DATABASE_URL + PORT
 * - Solid: + auth secrets, ORM config
 * - Battle-Tested: + Docker env, CI secrets, OAuth provider setup
 * - Vai (Premium): + BYOK AI keys, multi-provider OAuth, real-time, wallet config
 */

/* ── ENV Variable Definitions ─────────────────────────────────────────── */

export interface EnvVar {
  key: string;
  label: string;
  description: string;
  /** Which tiers need this variable */
  tiers: ('basic' | 'solid' | 'battle-tested' | 'vai')[];
  /** Default value (if any) */
  defaultValue?: string;
  /** Is this required for the app to run? */
  required: boolean;
  /** Can CLI auto-generate this? (e.g., random secrets) */
  autoGenerate?: 'secret' | 'uuid' | 'port';
  /** Provider console URL where user gets this key */
  providerUrl?: string;
  /** Step-by-step instructions to get the key */
  howToGet?: string[];
  /** What the key format looks like (for validation hint) */
  formatHint?: string;
  /** Category grouping */
  category: 'database' | 'auth' | 'server' | 'ai' | 'oauth' | 'crypto' | 'realtime' | 'storage';
  /** Validation regex */
  validation?: string;
}

