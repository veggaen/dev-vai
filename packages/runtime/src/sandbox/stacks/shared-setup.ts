/* ═══════════════════════════════════════════════════════════════════════
   GENERIC TIER-SPECIFIC SHELLS — Parameterized by domain config
   Used by MERN, T3, and Next.js stacks.
   ═══════════════════════════════════════════════════════════════════════ */

export interface DomainShellConfig {
  /** Component name, e.g., 'BookmarkManager' */
  componentName: string;
  /** Import path, e.g., './components/BookmarkManager' */
  componentImport: string;
  /** Tab ID, e.g., 'bookmarks' */
  tabId: string;
  /** Tab label, e.g., 'Bookmarks' */
  tabLabel: string;
  /** Lucide icon import fragment, e.g., 'Bookmark as BookmarkIcon' */
  tabIcon: string;
  /** JSX icon name, e.g., 'BookmarkIcon' */
  tabIconJsx: string;
  /** Whether domain component fills entire space (has own sidebar/scroll) */
  fullBleed?: boolean;
  /** Import path prefix ('./' for Vite, '../' for Next.js App Router) */
  importPrefix?: string;
  /** Add 'use client' directive (Next.js) */
  useClient?: boolean;
  /** Use default export (Next.js page) vs named export (Vite App) */
  defaultExport?: boolean;
  /** Override auth page import path (default: `${importPrefix}pages/AuthPage`) */
  authPageImport?: string;
}

/** Solid tier shell — Auth gate + domain component + UserMenu */
