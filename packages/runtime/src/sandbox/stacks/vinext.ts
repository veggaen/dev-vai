/**
 * LayoutShell — The Atlas Layout System.
 * Dual-mode grid: Compact (VSCode-like, zero gaps) vs Open (floating, shadows).
 * Resizable sidebar via pointer drag + keyboard (Ctrl+B).
 * Persists mode + sidebar width in localStorage.
 * Responsive: collapses sidebar below 768px.
 */
function makeLayoutShell(): string {
  return [
    `'use client';`,
    `import { useState, useEffect, useCallback, type ReactNode } from 'react';`,
    `import clsx from 'clsx';`,
    ``,
    `type LayoutMode = 'compact' | 'open';`,
    ``,
    `interface Props {`,
    `  header: ReactNode;`,
    `  sidebar?: ReactNode | null;`,
    `  children: ReactNode;`,
    `  defaultSidebarWidth?: number;`,
    `  className?: string;`,
    `}`,
    ``,
    `export default function LayoutShell({ header, sidebar = null, children, defaultSidebarWidth = 260, className }: Props) {`,
    `  const [mode, setMode] = useState<LayoutMode>('compact');`,
    `  const [sideW, setSideW] = useState(defaultSidebarWidth);`,
    `  const [dragging, setDragging] = useState(false);`,
    `  const [sidebarOpen, setSidebarOpen] = useState(true);`,
    `  const [isMobile, setIsMobile] = useState(false);`,
    `  const hasSidebar = sidebar !== null && sidebarOpen && !isMobile;`,
    `  const isOpen = mode === 'open';`,
    ``,
    `  useEffect(() => {`,
    `    const m = localStorage.getItem('vai-layout-mode') as LayoutMode | null;`,
    `    if (m === 'compact' || m === 'open') setMode(m);`,
    `    const w = localStorage.getItem('vai-sidebar-w');`,
    `    if (w) setSideW(Number(w));`,
    `    const check = () => setIsMobile(window.innerWidth < 768);`,
    `    check();`,
    `    window.addEventListener('resize', check);`,
    `    return () => window.removeEventListener('resize', check);`,
    `  }, []);`,
    ``,
    `  useEffect(() => { localStorage.setItem('vai-layout-mode', mode); }, [mode]);`,
    `  useEffect(() => { localStorage.setItem('vai-sidebar-w', String(sideW)); }, [sideW]);`,
    ``,
    `  const onSplitterDown = useCallback((e: React.PointerEvent) => {`,
    `    e.preventDefault();`,
    `    setDragging(true);`,
    `  }, []);`,
    ``,
    `  useEffect(() => {`,
    `    if (!dragging) return;`,
    `    document.body.style.cursor = 'col-resize';`,
    `    document.body.style.userSelect = 'none';`,
    `    const pad = isOpen ? 8 : 0;`,
    `    const onMove = (e: PointerEvent) => setSideW(Math.max(180, Math.min(420, e.clientX - pad)));`,
    `    const onUp = () => { setDragging(false); document.body.style.cursor = ''; document.body.style.userSelect = ''; };`,
    `    window.addEventListener('pointermove', onMove);`,
    `    window.addEventListener('pointerup', onUp);`,
    `    return () => { window.removeEventListener('pointermove', onMove); window.removeEventListener('pointerup', onUp); };`,
    `  }, [dragging, isOpen]);`,
    ``,
    `  useEffect(() => {`,
    `    const handler = (e: KeyboardEvent) => {`,
    `      if ((e.ctrlKey || e.metaKey) && e.key === 'b') { e.preventDefault(); setSidebarOpen(v => !v); }`,
    `    };`,
    `    window.addEventListener('keydown', handler);`,
    `    return () => window.removeEventListener('keydown', handler);`,
    `  }, []);`,
    ``,
    `  const gap = isOpen ? 8 : 0;`,
    `  const pad = isOpen ? 8 : 0;`,
    `  const rad = isOpen ? 12 : 0;`,
    ``,
    `  return (`,
    `    <div className={clsx('h-screen overflow-hidden', className)}`,
    `      style={{ display: 'grid',`,
    `        gridTemplateAreas: hasSidebar ? '"hdr hdr" "side main"' : '"hdr" "main"',`,
    `        gridTemplateColumns: hasSidebar ? sideW + 'px 1fr' : '1fr',`,
    `        gridTemplateRows: 'auto 1fr', gap, padding: pad,`,
    `        transition: 'gap 300ms ease, padding 300ms ease', scrollbarGutter: 'stable' as any }}>`,
    ``,
    `      <header style={{ gridArea: 'hdr', borderRadius: rad, transition: 'all 300ms ease',`,
    `        boxShadow: isOpen ? '0 2px 20px rgba(0,0,0,0.12)' : 'none', overflow: 'hidden', position: 'relative', zIndex: 20 }}>`,
    `        {header}`,
    `        <button onClick={() => setMode(m => m === 'compact' ? 'open' : 'compact')}`,
    `          aria-label={isOpen ? 'Compact mode' : 'Open mode'}`,
    `          className="absolute right-4 top-1/2 -translate-y-1/2 w-7 h-7 rounded-md border border-[var(--color-border)] bg-[var(--color-surface)] flex items-center justify-center text-[11px] cursor-pointer hover:border-[var(--color-primary)] hover:text-[var(--color-primary)] transition-all duration-200 z-30">`,
    `          {isOpen ? '\u229F' : '\u229E'}`,
    `        </button>`,
    `      </header>`,
    ``,
    `      {hasSidebar && (`,
    `        <aside role="region" aria-label="Sidebar" style={{ gridArea: 'side', borderRadius: rad,`,
    `          transition: 'all 300ms ease', boxShadow: isOpen ? '0 2px 20px rgba(0,0,0,0.10)' : 'none',`,
    `          overflow: 'hidden', position: 'relative', minWidth: 180 }}>`,
    `          {sidebar}`,
    `          <div onPointerDown={onSplitterDown} className="absolute top-0 bottom-0 flex items-center justify-center z-10"`,
    `            style={{ right: isOpen ? -8 : -2, width: isOpen ? 16 : 6, cursor: 'col-resize', touchAction: 'none' }}>`,
    `            <div className="w-0.5 h-6 rounded-full transition-colors duration-200"`,
    `              style={{ background: dragging ? 'var(--color-primary, #6366f1)' : 'transparent' }} />`,
    `          </div>`,
    `        </aside>`,
    `      )}`,
    ``,
    `      <main role="region" aria-label="Main content" className="overflow-auto"`,
    `        style={{ gridArea: 'main', borderRadius: rad, transition: 'all 300ms ease',`,
    `          boxShadow: isOpen ? '0 2px 20px rgba(0,0,0,0.10)' : 'none' }}>`,
    `        {children}`,
    `      </main>`,
    `    </div>`,
    `  );`,
    `}`,
    ``,
  ].join('\n');
}

/**
 * AmbientCanvas — Subtle floating particle network.
 * "Always Alive" per Master.md §4 — never a static page.
 */
function makeAmbientCanvas(): string {
  return [
    `'use client';`,
    `import { useEffect, useRef } from 'react';`,
    ``,
    `interface Props { color?: string; count?: number; opacity?: number; }`,
    ``,
    `export default function AmbientCanvas({ color = '#6366f1', count = 45, opacity = 0.35 }: Props) {`,
    `  const ref = useRef<HTMLCanvasElement>(null);`,
    ``,
    `  useEffect(() => {`,
    `    const c = ref.current;`,
    `    if (!c) return;`,
    `    const ctx = c.getContext('2d');`,
    `    if (!ctx) return;`,
    `    let raf: number;`,
    `    const dpr = window.devicePixelRatio || 1;`,
    `    type P = { x: number; y: number; vx: number; vy: number; r: number; a: number };`,
    `    const ps: P[] = [];`,
    ``,
    `    const resize = () => {`,
    `      const w = c.offsetWidth, h = c.offsetHeight;`,
    `      c.width = w * dpr; c.height = h * dpr;`,
    `      ctx.setTransform(dpr, 0, 0, dpr, 0, 0);`,
    `    };`,
    `    resize();`,
    `    window.addEventListener('resize', resize);`,
    ``,
    `    for (let i = 0; i < count; i++) ps.push({`,
    `      x: Math.random() * c.offsetWidth, y: Math.random() * c.offsetHeight,`,
    `      vx: (Math.random() - 0.5) * 0.35, vy: (Math.random() - 0.5) * 0.35,`,
    `      r: Math.random() * 1.5 + 0.5, a: Math.random() * opacity * 0.6 + opacity * 0.4,`,
    `    });`,
    ``,
    `    const draw = () => {`,
    `      const w = c.offsetWidth, h = c.offsetHeight;`,
    `      ctx.clearRect(0, 0, w, h);`,
    `      for (const p of ps) {`,
    `        p.x += p.vx; p.y += p.vy;`,
    `        if (p.x < 0) p.x = w; if (p.x > w) p.x = 0;`,
    `        if (p.y < 0) p.y = h; if (p.y > h) p.y = 0;`,
    `        ctx.globalAlpha = p.a;`,
    `        ctx.beginPath(); ctx.arc(p.x, p.y, p.r, 0, Math.PI * 2); ctx.fillStyle = color; ctx.fill();`,
    `      }`,
    `      for (let i = 0; i < ps.length; i++) for (let j = i + 1; j < ps.length; j++) {`,
    `        const dx = ps[i].x - ps[j].x, dy = ps[i].y - ps[j].y, d = Math.sqrt(dx * dx + dy * dy);`,
    `        if (d < 110) {`,
    `          ctx.globalAlpha = (1 - d / 110) * 0.14;`,
    `          ctx.beginPath(); ctx.moveTo(ps[i].x, ps[i].y); ctx.lineTo(ps[j].x, ps[j].y);`,
    `          ctx.strokeStyle = color; ctx.lineWidth = 0.6; ctx.stroke();`,
    `        }`,
    `      }`,
    `      ctx.globalAlpha = 1;`,
    `      raf = requestAnimationFrame(draw);`,
    `    };`,
    `    draw();`,
    `    return () => { cancelAnimationFrame(raf); window.removeEventListener('resize', resize); };`,
    `  }, [color, count, opacity]);`,
    ``,
    `  return <canvas ref={ref} className="fixed inset-0 w-full h-full pointer-events-none z-0" />;`,
    `}`,
    ``,
  ].join('\n');
}

/**
 * useScrollReveal — GSAP ScrollTrigger hook.
 * Dynamically imports GSAP to avoid SSR issues.
 * Attach data-reveal to elements that should animate in on scroll.
 */
function makeScrollRevealHook(): string {
  return [
    `'use client';`,
    `import { useEffect, useRef, type RefObject } from 'react';`,
    ``,
    `export function useScrollReveal<T extends HTMLElement = HTMLDivElement>(): RefObject<T | null> {`,
    `  const ref = useRef<T>(null);`,
    `  useEffect(() => {`,
    `    const el = ref.current;`,
    `    if (!el) return;`,
    `    let ctx: any;`,
    `    (async () => {`,
    `      const { default: gsap } = await import('gsap');`,
    `      const { ScrollTrigger } = await import('gsap/ScrollTrigger');`,
    `      gsap.registerPlugin(ScrollTrigger);`,
    `      ctx = gsap.context(() => {`,
    `        const items = el.querySelectorAll('[data-reveal]');`,
    `        if (!items.length) return;`,
    `        gsap.fromTo(Array.from(items), { opacity: 0, y: 36 }, {`,
    `          opacity: 1, y: 0, duration: 0.65, stagger: 0.07, ease: 'power3.out',`,
    `          scrollTrigger: { trigger: el, start: 'top 85%' },`,
    `        });`,
    `      }, el);`,
    `    })();`,
    `    return () => { if (ctx) ctx.revert(); };`,
    `  }, []);`,
    `  return ref;`,
    `}`,
    ``,
  ].join('\n');
}

/* ═══════════════════════════════════════════════════════════════
   §3b  CURSOR BORDER BOX — Animated hover border (Master.md)
   ═══════════════════════════════════════════════════════════════ */

function makeCursorBorderBox(): string {
  return [
    `'use client';`,
    `import { useEffect, useRef } from 'react';`,
    ``,
    `interface Props { color?: string; }`,
    ``,
    `export default function CursorBorderBox({ color = '#7c3aed' }: Props) {`,
    `  const ref = useRef<HTMLDivElement>(null);`,
    ``,
    `  useEffect(() => {`,
    `    const box = ref.current;`,
    `    if (!box) return;`,
    `    const sel = 'button, a, input, textarea, [data-hover], [role="button"]';`,
    `    let activeEl: Element | null = null;`,
    `    let cleanup: (() => void) | null = null;`,
    `    let raf = 0;`,
    ``,
    `    (async () => {`,
    `      const { default: gsap } = await import('gsap');`,
    ``,
    `      function snapToEl(el: Element, animate = true) {`,
    `        const rect = el.getBoundingClientRect();`,
    `        const cs = getComputedStyle(el);`,
    `        const br = parseFloat(cs.borderRadius) || 8;`,
    `        const props = { x: rect.left - 3, y: rect.top - 3, width: rect.width + 6, height: rect.height + 6, borderRadius: br };`,
    `        if (animate) {`,
    `          gsap.to(box, { ...props, duration: 0.3, ease: 'power3.out', onStart: () => { box.style.opacity = '1'; } });`,
    `        } else {`,
    `          gsap.set(box, props);`,
    `        }`,
    `      }`,
    ``,
    `      const onOver = (e: MouseEvent) => {`,
    `        const el = (e.target as Element)?.closest?.(sel);`,
    `        if (!el) return;`,
    `        activeEl = el;`,
    `        snapToEl(el, true);`,
    `      };`,
    ``,
    `      const onOut = (e: MouseEvent) => {`,
    `        const related = (e.relatedTarget as Element)?.closest?.(sel);`,
    `        if (!related) {`,
    `          activeEl = null;`,
    `          gsap.to(box, { opacity: 0, duration: 0.2, ease: 'power2.in' });`,
    `        }`,
    `      };`,
    ``,
    `      const onScroll = () => {`,
    `        if (!activeEl) return;`,
    `        if (!activeEl.isConnected) { activeEl = null; box.style.opacity = '0'; return; }`,
    `        cancelAnimationFrame(raf);`,
    `        raf = requestAnimationFrame(() => { if (activeEl) snapToEl(activeEl, false); });`,
    `      };`,
    ``,
    `      document.addEventListener('mouseover', onOver);`,
    `      document.addEventListener('mouseout', onOut);`,
    `      window.addEventListener('scroll', onScroll, { capture: true, passive: true });`,
    `      cleanup = () => {`,
    `        document.removeEventListener('mouseover', onOver);`,
    `        document.removeEventListener('mouseout', onOut);`,
    `        window.removeEventListener('scroll', onScroll, true);`,
    `        cancelAnimationFrame(raf);`,
    `      };`,
    `    })();`,
    ``,
    `    return () => { cleanup?.(); };`,
    `  }, []);`,
    ``,
    `  return (`,
    `    <div ref={ref} style={{`,
    `      position: 'fixed', top: 0, left: 0,`,
    `      pointerEvents: 'none', zIndex: 9999,`,
    `      border: \`2px solid \${color}60\`,`,
    `      boxShadow: \`0 0 14px \${color}25, inset 0 0 14px \${color}08\`,`,
    `      opacity: 0, willChange: 'transform, width, height, opacity',`,
    `    }} />`,
    `  );`,
    `}`,
    ``,
  ].join('\n');
}

/* ═══════════════════════════════════════════════════════════════
   §3c  ANIMATED COUNTER — Number count-up animation
   ═══════════════════════════════════════════════════════════════ */

function makeAnimatedCounter(): string {
  return [
    `'use client';`,
    `import { useEffect, useState, useRef } from 'react';`,
    ``,
    `interface Props { value: string; className?: string; }`,
    ``,
    `export default function AnimatedCounter({ value, className }: Props) {`,
    `  const [display, setDisplay] = useState(value);`,
    `  const ref = useRef<HTMLSpanElement>(null);`,
    `  const hasRun = useRef(false);`,
    ``,
    `  useEffect(() => {`,
    `    if (hasRun.current) return;`,
    `    const numeric = parseFloat(value.replace(/[^0-9.]/g, ''));`,
    `    if (isNaN(numeric)) { setDisplay(value); return; }`,
    `    hasRun.current = true;`,
    `    const prefix = value.match(/^[^0-9]*/)?.[0] || '';`,
    `    const suffix = value.match(/[^0-9.]*$/)?.[0] || '';`,
    `    const hasDec = value.includes('.');`,
    `    const dur = 1200;`,
    `    const start = performance.now();`,
    ``,
    `    function tick(now: number) {`,
    `      const p = Math.min((now - start) / dur, 1);`,
    `      const eased = 1 - Math.pow(1 - p, 3);`,
    `      const cur = numeric * eased;`,
    `      const fmt = hasDec ? cur.toFixed(1) : Math.round(cur).toLocaleString();`,
    `      setDisplay(prefix + fmt + suffix);`,
    `      if (p < 1) requestAnimationFrame(tick);`,
    `    }`,
    `    requestAnimationFrame(tick);`,
    `  }, [value]);`,
    ``,
    `  return <span ref={ref} className={className}>{display}</span>;`,
    `}`,
    ``,
  ].join('\n');
}

