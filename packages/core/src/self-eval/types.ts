/**
 * Self-evaluation umbrella — types.
 *
 * See docs/capabilities/self-evaluation.md for design doc.
 *
 * Architecture (3 pieces):
 *   1. ResponsePredicate — { id, derive(input), check(candidate, input, history) }
 *   2. SelfEvaluator     — runs registered predicates, returns SelfEvalVerdict
 *   3. constraint-checking predicate registration set (this turn's only sub-cap)
 *
 * Hard guarantees:
 *   - At most ONE revision per turn (anti-pattern #13 mitigation).
 *   - All checks are pure functions of (input, history, candidate). No I/O.
 *   - derive(input) returning null is a no-op (predicate doesn't apply).
 */

import type { Message } from '../models/adapter.js';

/** Result of running a single predicate's check() against a candidate. */
export type PredicateResult =
  | { ok: true }
  | { ok: false; hint: string };

/**
 * A predicate compiled from a specific input.
 * `derive(input)` produces this; `null` means the predicate doesn't apply.
 */
export interface CompiledPredicate {
  /** Stable id of the originating ResponsePredicate (for logs / failure attribution). */
  readonly id: string;
  /** Human-readable description of what this compiled predicate is checking, for trace logs. */
  readonly description: string;
  /** Run the check against a candidate response. */
  check(candidate: string, input: string, history: readonly Message[]): PredicateResult;
}

/**
 * A response predicate factory. `derive` is a cheap parser of the input
 * deciding whether the predicate applies; if it returns null, the predicate
 * is a no-op for this turn.
 */
export interface ResponsePredicate {
  /** Stable id (kebab-case, used in ResponseMeta.selfEvalFailedPredicates). */
  readonly id: string;
  /** Cheap parser. Returns null if this predicate doesn't apply to this input. */
  derive(input: string, history: readonly Message[]): CompiledPredicate | null;
}

/**
 * Final verdict from a self-evaluation pass.
 *
 *  - 'pass'             — all applicable predicates passed; emit candidate.
 *  - 'revise-applied'   — first draft failed, second draft passed; emit second.
 *  - 'flag-uncertain'   — second draft also failed (cap fired). Emit second draft,
 *                         annotate ResponseMeta. Caller decides what to surface.
 */
export type SelfEvalVerdictKind = 'pass' | 'revise-applied' | 'flag-uncertain';

/**
 * Trace record for a single draft's evaluation, written to --draft-trace logs.
 * Includes cap-suppressed diagnostic third drafts (see Addition #2 in design).
 */
export interface DraftTraceRecord {
  /** When in the cycle this draft was produced. */
  readonly stage: 'draft-1' | 'draft-2' | 'draft-3-cap-suppressed';
  /** The candidate string at this stage. */
  readonly candidate: string;
  /** Verdict for this draft alone. */
  readonly verdict: 'pass' | 'fail';
  /** IDs of failed predicates (empty when verdict is 'pass'). */
  readonly failedPredicates: readonly string[];
  /** Hint passed forward to the next revision attempt (null on pass / on cap-suppression). */
  readonly hint: string | null;
  /** True only for the diagnostic-only third draft that is generated, traced, and discarded. */
  readonly capSuppressed?: boolean;
}

export interface SelfEvalVerdict {
  /** Final disposition. */
  readonly kind: SelfEvalVerdictKind;
  /** The string the engine SHOULD emit (may be the second draft, never the third). */
  readonly emit: string;
  /** Concatenated hint from all failing predicates on the most recent evaluated draft. */
  readonly lastHint: string | null;
  /** Predicates that failed on the most recent evaluated draft (for ResponseMeta). */
  readonly failedPredicates: readonly string[];
  /** Whether revision was applied (driven by the single-revision cap). */
  readonly revisionApplied: boolean;
  /** Whether the cap fired (second draft also failed). */
  readonly capFired: boolean;
  /**
   * Per-stage trace, in order. Always at least 1 entry (draft-1).
   * If revisionApplied: also draft-2.
   * If a diagnostic third draft was generated by an external --draft-trace caller,
   * it is added by that caller (not by the SelfEvaluator itself, since the
   * evaluator does not generate drafts).
   */
  readonly trace: readonly DraftTraceRecord[];
}
