        className="ml-1 inline-flex h-5 min-w-5 cursor-pointer items-center justify-center rounded-full border border-violet-500/30 bg-violet-500/14 px-1.5 text-[10px] font-semibold text-violet-200 shadow-[0_0_0_1px_rgba(124,58,237,0.14)] transition-all hover:-translate-y-px hover:border-violet-400/45 hover:bg-violet-500/22 hover:text-white"
