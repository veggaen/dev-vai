const INLINE_TOKEN_REGEX = /(\[[^\]]+\]\([^)]+\)|\[\d+\]|`[^`]+`|\*\*[^*]+\*\*|\*[^*]+\*)/g;

function renderInlineWithCitations({
  text,
  sources,
  hoveredCitationKey,
  onHoverCitation,
  keyPrefix,
}: {
  text: string;
  sources?: SearchSourceUI[];
  hoveredCitationKey: string | null;
  onHoverCitation: (key: string | null) => void;
  keyPrefix: string;
}) {
  const nodes: React.ReactNode[] = [];
  let lastIndex = 0;
  let tokenMatch: RegExpExecArray | null;

  INLINE_TOKEN_REGEX.lastIndex = 0;

  while ((tokenMatch = INLINE_TOKEN_REGEX.exec(text)) !== null) {
    if (tokenMatch.index > lastIndex) {
      nodes.push(
        <span key={`${keyPrefix}-text-${lastIndex}`}>
          {text.slice(lastIndex, tokenMatch.index)}
        </span>,
      );
    }

    const token = tokenMatch[0];
    const citationMatch = token.match(/^\[(\d+)\]$/);
    const linkMatch = token.match(/^\[([^\]]+)\]\(([^)]+)\)$/);
    const codeMatch = token.match(/^`([^`]+)`$/);
    const boldMatch = token.match(/^\*\*([^*]+)\*\*$/);
    const italicMatch = token.match(/^\*([^*]+)\*$/);
    const tokenKey = `${keyPrefix}-token-${tokenMatch.index}`;

    if (citationMatch) {
      const sourceIndex = Number.parseInt(citationMatch[1], 10) - 1;
      const source = sources?.[sourceIndex];
      if (source) {
        const instanceKey = `${tokenKey}-cite-${sourceIndex}`;
        nodes.push(
          <CitationBadge
            key={instanceKey}
            index={sourceIndex + 1}
            source={source}
            isHovered={hoveredCitationKey === instanceKey}
            onEnter={() => onHoverCitation(instanceKey)}
            onLeave={() => onHoverCitation(null)}
          />,
        );
      } else {
        nodes.push(<span key={tokenKey}>{token}</span>);
      }
    } else if (linkMatch) {
      nodes.push(
        <a
          key={tokenKey}
          href={linkMatch[2]}
          target="_blank"
          rel="noopener noreferrer"
          className="font-medium text-sky-300 transition-colors hover:text-sky-200 hover:underline"
        >
          {linkMatch[1]}
        </a>,
      );
    } else if (codeMatch) {
      nodes.push(
        <code
          key={tokenKey}
          className="rounded-md border border-white/10 bg-black/20 px-1.5 py-0.5 font-mono text-[0.92em] text-zinc-100"
        >
          {codeMatch[1]}
        </code>,
      );
    } else if (boldMatch) {
      nodes.push(
        <strong key={tokenKey} className="font-semibold text-zinc-50">
          {boldMatch[1]}
        </strong>,
      );
    } else if (italicMatch) {
      nodes.push(
        <em key={tokenKey} className="italic text-zinc-100/90">
          {italicMatch[1]}
        </em>,
      );
    } else {
      nodes.push(<span key={tokenKey}>{token}</span>);
    }

    lastIndex = tokenMatch.index + token.length;
  }

  if (lastIndex < text.length) {
    nodes.push(
      <span key={`${keyPrefix}-tail`}>
        {text.slice(lastIndex)}
      </span>,
    );
  }

  return nodes;
}

